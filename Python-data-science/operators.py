# Operators

"""
    Order of precedence
    --------------------------------------------------------
    ()  : paranthesis 
    **  : Exponent
    *   : Multiplication
    /   : Division
    +   : Addition
    -   : Subtraction 
"""

print(5+10) # Addition
print(5-10) # Subtraction
print(5/10) # Division
print(5//2) # Floor division : No of times 2 go evenly into 5
print(5*10) # Multiplication
print(52%10)# Modulus : Remainder after division
print(2**3) # Exponent